import { Platform, NativeModules } from 'react-native';

/* ============================================================
 * Détection langue device — fallback 'fr'
 * ============================================================ */
export function getDeviceLanguage() {
  try {
    const locale =
      (Platform.OS === 'ios'
        ? NativeModules.SettingsManager?.settings?.AppleLocale ||
          NativeModules.SettingsManager?.settings?.AppleLanguages?.[0]
        : NativeModules.I18nManager?.localeIdentifier) || 'fr';
    const code = String(locale).toLowerCase().slice(0, 2);
    // Refocalisation 2026-05-05 : 4 langues uniquement.
    if (['fr', 'nl', 'en', 'de'].includes(code)) return code;
    return 'fr';
  } catch (_) {
    return 'fr';
  }
}

